    // API
    public function salutationList()
    {
        $salutations = Salutation::withCount('employees')->orderBy('name')->get();
        
        return response()->json($salutations);
    }

    public function salutationCreate(Request $request)
    {
        $data = $request->validate([
            'name' => 'required|string|max:255',
            'description' => 'nullable|string'
        ]);

        try {
            $salutation = Salutation::create($data);
        } catch (Exception $e) {
            return response()->json([
                'message' => $e->getMessage()
            ], 500);
        }

        return response()->json([
            'message' => 'Salutation added successfully',
            'data' => $salutation
        ], 201);
    }

    public function salutationEdit(Request $request)
    {
        $request->validate([
            'id' => 'required|exists:salutations,id',
            'name' => 'required|string|max:255',
            'description' => 'nullable|string'
        ]);

        try {
            $salutation = Salutation::find($request->id);

            $salutation->update([
                'name' => $request->name,
                'description' => $request->description,
            ]);

        } catch (Exception $e) {
            return response()->json([
                'message' => $e->getMessage()
            ], 500);
        }

        return response()->json([
            'message' => 'Salutation updated successfully',
            'data' => $salutation
        ]);
    }

    public function salutationDelete($id)
    {
        request()->validate([
            'id' => 'exists:salutations,id'
        ]);
        
        $salutation = Salutation::find($id);

        try {
            $salutation->delete();
        } catch (Exception $e) {
            return response()->json([
                'message' => $e->getMessage()
            ], 500);
        }

        return response()->json([
            'message' => 'Salutation deleted successfully',
        ]);
    }
}